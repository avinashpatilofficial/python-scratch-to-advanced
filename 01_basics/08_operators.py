# 8️⃣ Operators in Python

a, b = 10, 3

# Arithmetic Operators
print(a + b)   # Addition
print(a - b)   # Subtraction
print(a * b)   # Multiplication
print(a / b)   # Division
print(a % b)   # Modulus
print(a ** b)  # Exponent
print(a // b)  # Floor Division

# Comparison Operators
print(a == b, a != b, a > b, a < b, a >= b, a <= b)

# Logical Operators
print(a > 5 and b < 5)
print(a > 5 or b > 10)
print(not(a < b))

# Assignment Operators
a += 2
print(a)

# Membership Operators
print("A" in "Avinash")

# Identity Operators
x = [1, 2]
y = [1, 2]
print(x == y, x is y)
