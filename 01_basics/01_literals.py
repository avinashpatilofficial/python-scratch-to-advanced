# 1️⃣ Literals in Python
# Literals are fixed values directly written in the code.

# Numeric Literals
integer_num = 10
float_num = 3.14
complex_num = 2 + 3j

# String Literal
text = "Avinash"

# Boolean Literal
is_active = True

# Special Literal
data = None

# Collection Literals
my_list = [1, 2, 3]
my_tuple = (4, 5, 6)
my_dict = {"name": "Avi", "age": 25}
my_set = {7, 8, 9}

print(integer_num, float_num, complex_num, text, is_active, data)
print(my_list, my_tuple, my_dict, my_set)
